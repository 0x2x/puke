Embed parameters:
  {author: hi} - Sets the author
    use && for multiple parts
    Example: {author: hi && https://url.com && https://url.com}
  {title: hi} - Sets the title
  {description: okay} - sets the description
  {footer: hi} - Sets the footer
    Example: {footer: hi && https://url.com}
  {url:  https://url.com} - Sets the URL
  {timestamp} - Sets the embed timestamp
  {color: HEX} - Sets the embed color
  {field: hi && okay && true} - Adds field
    Example: {field: hi && gb && false}
  {image: https://url.com} - Adds an image
  {thumbnail: https://url.com} - Adds an thumbnail
  {message: hi} - adds an optional message   

  Examples:
    {embed}$v{title: hi}$v{description: okay bye}$v{field: okay bye && bye bitch && false}$v{field: bye bye && bye bitch && false}$v{color: 36393f}$v{timestamp}

